package org.javarevision.Collections.Lists.ArrayList.Example3_twoarraylists;

public class Student {
    String name;
    int id;
    public Student(String name, int id){
        this.name = name;
        this.id = id;
    }
    public String getName(){
        return name;
    }
    public int getId(){
        return id;
    }
}
